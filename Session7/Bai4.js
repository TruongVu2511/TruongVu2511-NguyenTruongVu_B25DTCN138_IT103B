let players = [
    "P001-Nguyễn Văn A-Thủ môn",
    "P002-Trần Thị B-Hậu vệ",
    "P003-Lê Văn C-Hậu vệ",
    "P004-Phạm Văn D-Tiền vệ",
    "P005-Hoàng Thị E-Tiền đạo",
    "P006-Vũ Minh F-Tiền đạo",
    "P007-Đặng Văn G-Thủ môn"
];

function printTeamRoster() {
    for (let i = 0; i < players.length; i++) {
        let info = players[i].split("-");
        console.log(
            (i + 1) + ". " + info[0] + " | " + info[1] + " | " + info[2]
        );
    }
}

function countPlayerByPosition(players) {
    let result = {};

    for (let i = 0; i < players.length; i++) {
        let position = players[i].split("-")[2];

        if (result[position] === undefined) {
            result[position] = 1;
        } else {
            result[position]++;
        }
    }

    return result;
}

function hasGoalkeeper() {
    for (let i = 0; i < players.length; i++) {
        if (players[i].split("-")[2] === "Thủ môn") {
            return true;
        }
    }
    return false;
}

printTeamRoster();
console.log(countPlayerByPosition(players));
console.log(hasGoalkeeper());
